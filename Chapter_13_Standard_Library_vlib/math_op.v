import math

// where `x' and `y' are f64
fn main() {
	x := 1.0
	y := 2.0
	println(math.e)
	println(math.pi)
	println(math.sqrt(x))
	println(math.log(x))
	println(math.log10(x))
	println(math.exp(x))
	println(math.abs(x))
	println(math.floor(x))
	println(math.ceil(x))
	println(math.pow(x, y))
}

