fn main() {
	mut field := gun()
	print_field(field)
	for {
		mut new_field := []array_int
		for i, line in field {
			new_field << [0].repeat(line.len)
		}
		for i, line in field {
			if i == 0 || i == field.len - 1 {continue}
			for j, cell in line {
				if j == 0 || j == line.len - 1 {continue}
				// calculate sum of all neighbouring cells
				moore_sum := (
				field[i - 1] [j - 1] + field[i - 1] [j] + field[i - 1] [j + 1] +
				field[i] [j - 1] + field[i] [j + 1] +
				field[i + 1] [j - 1] + field[i + 1] [j] + field[i + 1] [j + 1]
				)
				// apply rule for living cell in next generation
				v := if cell == 1 { // a living cell with 2 or 3 living neighbours stays alive
					(moore_sum in [2, 3])
				}
				else { // a dead cell with 3 living neighbours becomes alive
					(moore_sum == 3)
				}
				new_field[i] [j] = if v { 1 } else { 0 }
			}
		}
		field = new_field // copy new generation over
		print_field(field) // print new generation
		time.sleep_ms(100)
	}
}
